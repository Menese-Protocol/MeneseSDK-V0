const SOVEREIGN_SEND = "fxjsq-raaaa-aaaab-agdaa-cai";
const ICP_SOL_SWAP   = "w2vjc-2yaaa-aaaab-ae6zq-cai";
const SOL_BORROW_V3  = "p7teu-wyaaa-aaaab-afnvq-cai";
