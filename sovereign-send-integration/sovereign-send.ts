const sovereignSendIDL = ({ IDL }: any) => {
  const BorrowParams = IDL.Record({
    iasolExpected: IDL.Nat64, // internal param — handled by canister
    iausdToMint: IDL.Nat64,
    minUsdcOut: IDL.Nat64,
    userPubkey: IDL.Vec(IDL.Nat8),
    userUsdcAta: IDL.Vec(IDL.Nat8),
  });

  const TreasuryType = IDL.Variant({
    IcpSolSwap: IDL.Null,
    SolBorrow: IDL.Null,
  });

  return IDL.Service({
    getMyAddress: IDL.Func([], [IDL.Record({
      address: IDL.Text,
      publicKeyBytes: IDL.Vec(IDL.Nat8),
    })], []),

    getFeeTreasuryAddress: IDL.Func([], [IDL.Text], []),
    getAdmin: IDL.Func([], [IDL.Principal], ["query"]),
    getRegisteredTreasuries: IDL.Func([], [IDL.Vec(IDL.Tuple(IDL.Principal, IDL.Text))], ["query"]),
    init: IDL.Func([], [], []),

    registerTreasury: IDL.Func(
      [IDL.Principal, IDL.Text, TreasuryType],
      [IDL.Variant({ ok: IDL.Null, err: IDL.Text })],
      [],
    ),

    // Sign-only: caller provides blockhash, broadcasts TX themselves
    signSend: IDL.Func(
      [IDL.Text, IDL.Nat64, IDL.Text],
      [IDL.Variant({
        ok: IDL.Record({
          signedTxBase64: IDL.Text,
          sendAmount: IDL.Nat64,
          feeAmount: IDL.Nat64,
          txSignature: IDL.Text,
        }),
        err: IDL.Text,
      })],
      [],
    ),

    // Autonomous: canister fetches blockhash + broadcasts via HTTP outcall
    sendSol: IDL.Func(
      [IDL.Text, IDL.Nat64],
      [IDL.Variant({
        ok: IDL.Record({
          txSignature: IDL.Text,
          sendAmount: IDL.Nat64,
          feeAmount: IDL.Nat64,
        }),
        err: IDL.Text,
      })],
      [],
    ),

    depositSol: IDL.Func(
      [IDL.Principal, IDL.Nat64, IDL.Opt(BorrowParams)],
      [IDL.Variant({
        ok: IDL.Record({
          txSignature: IDL.Text,
          depositId: IDL.Nat,
        }),
        err: IDL.Text,
      })],
      [],
    ),

    // ── Derivation Shield (caller-pays economics) ──
    getShieldStatus: IDL.Func([], [IDL.Record({
      cycleBalance: IDL.Nat,
      minReserve: IDL.Nat,
      circuitBreakerActive: IDL.Bool,
      cachedUsers: IDL.Nat,
      allowlistSize: IDL.Nat,
      vouchedUsers: IDL.Nat,
      derivationFee: IDL.Nat,
      totalCyclesCollected: IDL.Nat,
      totalPaidDerivations: IDL.Nat,
    })], ["query"]),

    addAllowedCaller: IDL.Func(
      [IDL.Principal],
      [IDL.Variant({ ok: IDL.Null, err: IDL.Text })],
      [],
    ),

    removeAllowedCaller: IDL.Func(
      [IDL.Principal],
      [IDL.Variant({ ok: IDL.Null, err: IDL.Text })],
      [],
    ),

    // ── Partner User Registration ──
    // Allowlisted partner canisters can register their users' principals.
    // Registered users call sovereign_send directly from their frontend
    // — no inter-canister hop, faster execution.
    registerPartnerUsers: IDL.Func(
      [IDL.Vec(IDL.Principal)],
      [IDL.Variant({ ok: IDL.Nat, err: IDL.Text })],
      [],
    ),

    removePartnerUsers: IDL.Func(
      [IDL.Vec(IDL.Principal)],
      [IDL.Variant({ ok: IDL.Nat, err: IDL.Text })],
      [],
    ),
  });
};

